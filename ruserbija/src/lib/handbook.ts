/** Все категории (для навигации) */
export const HANDBOOK_CATEGORIES = HANDBOOK.map(c => ({
  slug: c.slug, emoji: c.emoji, title: c.title, description: c.description,
  count: c.entries.length,
}));

/** Найти категорию по slug */
export function getCategory(slug: string): HandbookCategory | undefined {
  return HANDBOOK.find(c => c.slug === slug);
}

/** Найти запись по slug категории и записи */
export function getEntry(categorySlug: string, entrySlug: string): HandbookEntry | undefined {
  return getCategory(categorySlug)?.entries.find(e => e.slug === entrySlug);
}

/** Плоский список всех записей с привязкой к категории */
export function allEntries(): { category: HandbookCategory; entry: HandbookEntry }[] {
  return HANDBOOK.flatMap(c => c.entries.map(e => ({ category: c, entry: e })));
}
